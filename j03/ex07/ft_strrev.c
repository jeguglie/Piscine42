char	*ft_strrev(char *str)
{
	int i;
	int a;
	int temp;

	i = 0;
	a = 0;
	while (str[i])
		i++;
	while (i > a)
	{
		i--;
		temp = str[i];
		str[i] = str[a];
		str[a] = temp;
		a++;
	}
	return (str);
}
