#include "do_op.h"

void	ft_putnbr(long int nb)
{
	if (nb > 2147483647 || nb < -2147483648)
		return ;
	if (nb < 0)
	{
		if (nb == -2147483648)
		{
			ft_putstr("-2147483648");
			return ;
		}
		ft_putchar('-');
		nb = nb * (-1);
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + '0');
}
