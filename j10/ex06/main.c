#include "do_op.h"

int		main(int argc, char **argv)
{
	if (argc != 4)
		return (0);
	if (argv[2][0] == '+' || argv[2][0] == '-' ||
		argv[2][0] == '*' || argv[2][0] == '/' || argv[2][0] == '%')
	{
		if ((ft_atoi(argv[1]) > 2147483647 || ft_atoi(argv[1]) < -2147483648) ||
			(ft_atoi(argv[3]) > 2147483647 || ft_atoi(argv[3]) < -2147483648))
				return (0);
		do_op(ft_atoi(argv[1]), argv[2][0], ft_atoi(argv[3]));
		ft_putchar('\n');
	}
		
	return (0);
}
