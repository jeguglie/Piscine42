int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int j;
	int	i;
	int k;
	int m;

	i = 0;
	j = 0;
	k = 0;
	m = n;
	if (s1[i] == s2[j] && ((s1[i + 1] == '\0' && s2[j + 1] == '\0') || n == 0))
		return (0);
	while (k < m && (s1[i] == s2[j] &&
				(s1[i + 1] != '\0') && s2[j + 1] != '\0'))
	{
		i++;
		j++;
		k++;
	}
	if (s1[i] == s2[j])
		return (0);
	if (s1[i] > s2[j])
		return (s1[i] - s2[j]);
	if (s1[i] < s2[j])
		return (s1[i] - s2[j]);
	return (0);
}
